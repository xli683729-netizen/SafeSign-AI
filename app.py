# --- Helper Functions ---
def encode_image(image_bytes):
    return base64.b64encode(image_bytes).decode('utf-8')

def redact_sensitive_info(text):
    """PII Protection: Scrus emails and currency amounts"""
    text = re.sub(r'\S+@\S+', '[EMAIL REDACTED]', text)
    text = re.sub(r'\$\d+(?:,\d+)*(?:\.\d+)?', '[AMOUNT REDACTED]', text)
    return text

def create_pdf(text):
    """Generates PDF with character encoding protection"""
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", 'B', size=16)
    pdf.cell(200, 10, txt="SafeSign AI: Legal Audit Report", ln=True, align='C')
    pdf.ln(10)
    pdf.set_font("Arial", 'B', size=10)
    pdf.multi_cell(0, 5, txt="IMPORTANT: This report is generated by AI and is NOT a substitute for professional legal counsel.")
    pdf.ln(5)
    pdf.set_font("Arial", size=11)
    # Character Shield logic
    safe_text = text.replace('\u2013', '-').replace('\u2014', '-').replace('\u2018', "'").replace('\u2019', "'").replace('\u201c', '"').replace('\u201d', '"')
    clean_text = safe_text.encode('latin-1', 'ignore').decode('latin-1')
    pdf.multi_cell(0, 8, txt=clean_text)
    return pdf.output(dest="S").encode("latin-1")

