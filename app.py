# --- Enhanced Ingestion with REAL-TIME PROGRESS ---
if uploaded_file and st.session_state.contract_context == "":
    with st.status("Initializing document scan...", expanded=True) as status:
        file_type = uploaded_file.type
        raw_text = ""
        try:
            if "image" in file_type:
                st.write("📷 Performing OCR scan...")
                base64_img = encode_image(uploaded_file.read())
                res = client.chat.completions.create(
                    model="deepseek-chat",
                    messages=[{"role": "user", "content": [{"type": "text", "text": "Extract all text precisely."}, {"type": "image_url", "image_url": {"url": f"data:image/jpeg;base64,{base64_img}"}}]}]
                )
                raw_text = res.choices[0].message.content
            elif "pdf" in file_type:
                with pdfplumber.open(uploaded_file) as pdf:
                    total_pages = len(pdf.pages)
                    # This placeholder creates the "Live Ticker" effect
                    progress_bar = st.empty() 
                    for i, page in enumerate(pdf.pages):
                        # Updating the UI in every loop
                        progress_bar.info(f"📖 Ingesting content: Page {i+1} of {total_pages}...")
                        page_text = page.extract_text()
                        if page_text:
                            raw_text += page_text + "\n"
                    progress_bar.empty() # Clear ticker when done
            elif "wordprocessingml" in file_type:
                st.write("📝 Parsing Word document...")
                doc = docx.Document(uploaded_file)
                raw_text = "\n".join([p.text for p in doc.paragraphs])
            
            if privacy_on:
                st.write("🔒 Scrubbing PII data...")
                raw_text = redact_sensitive_info(raw_text)
            
            st.session_state.contract_context = raw_text
            status.update(label="Document Successfully Loaded!", state="complete", expanded=False)
        except Exception as e:
            st.error(f"Processing Error: {str(e)}")

